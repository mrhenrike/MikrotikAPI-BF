def smb_port_open(host: str, port: int = 445, timeout: float = 5.0) -> bool:
    try:
        s = socket.create_connection((host, port), timeout=timeout)
        s.close()
        return True
    except OSError:
        return False


def smb_negotiate_probe(host: str, port: int = 445, timeout: float = 5.0) -> Tuple[bool, str]:
    """Send SMB negotiation; return (responded, detail)."""
    try:
        s = socket.create_connection((host, port), timeout=timeout)
        s.sendall(SMB_NEGOTIATION_REQUEST)
        s.settimeout(timeout)
        resp = s.recv(512)
        s.close()
        if resp:
            return True, f"SMB negotiation response ({len(resp)} bytes)"
        return False, "No SMB response"
    except OSError as exc:
        return False, str(exc)


