def parse_ros_version(ver: str) -> Optional[Tuple[int, int, int]]:
    if not ver:
        return None
    parts = ver.split(".")
    try:
        return (int(parts[0]), int(parts[1]), int(parts[2]) if len(parts) > 2 else 0)
    except (ValueError, IndexError):
        return None


def version_in_27686_range(ver: str) -> Optional[str]:
    """Return 'low', 'high', or None if outside EDB-51931 ranges."""
    p = parse_ros_version(ver)
    if not p:
        return None
    if (6, 40, 5) <= p <= (6, 44, 99):
        return "low"
    if (6, 48, 1) <= p <= (6, 49, 10):
        return "high"
    return None
